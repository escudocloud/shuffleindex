package base.bptree;

/**
 * The class Split
 * @author Tommaso
 *
 * @param <Key>
 * @param <Value>
 */
class Split<Key extends Comparable<? super Key>, Value> {
	
	public final Key key;
	/** The left node */
	public final Node<Key, Value> left;
	/** The right node */
	public final Node<Key, Value>  right;
 
	/**
	 * The class constructor
	 * 
	 * @param k
	 * @param l the left node
	 * @param r the right node
	 */
	public Split(Key k, Node<Key, Value>  l, Node<Key, Value>  r) {
	    key = k; left = l; right = r;
	}
	
}
